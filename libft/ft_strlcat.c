#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t n)
{
	size_t	len_dst;
	size_t	len_src;

	len_dst = ft_strlen(dst);
	len_src = ft_strlen(src);
	if (n <= len_dst)
		return (len_src + n);
	n = n - (len_dst + 1);
	ft_strncat(dst, src, n);
	return (len_src + len_dst);
}
